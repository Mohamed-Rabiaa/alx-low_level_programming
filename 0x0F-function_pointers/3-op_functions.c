/**
 *op_add - calculates the sum of 2 integers
 *@a: the first integer
 *@b: the second integer
 *
 *Return: the sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 *op_sub - calculates the difference of 2 integers
 *@a: the first integer
 *@b: the second integer
 *
 *Return: the difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - calculates the multiplication of 2 integers
 *@a: the first integer
 *@b: the second integer
 *
 *Return: the product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *op_div  - calculates the division of 2 integers
 *@a: the first integer
 *@b: the second integer
 *
 *Return: the result of the division of a by b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 *op_mod - returns the remainder of the division of 2 integers
 *@a: the first integer
 *@b: the second integer
 *
 *Return: the remainder of the division of a by b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
